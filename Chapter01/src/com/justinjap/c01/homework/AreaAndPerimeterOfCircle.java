package com.justinjap.c01.homework;

public class AreaAndPerimeterOfCircle {

	public static void main(String[] args) {
		System.out.println("Area = Radius x Radius x PI");
		System.out.println((5.50 * 5.50 * Math.PI) + " = 5.5 x 5.5 x PI");
		System.out.println("Perimeter = 2 x Radius x PI");
		System.out.println((2.0 * 5.50 * Math.PI) + " = 2 x 5.5 x PI");
	}
}
